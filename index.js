app.post("/register", async (req, res) => {
  const { username, password, confirmPassword } = req.body;

  // 1Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å–æ–≤–ø–∞–¥–∞—é—Ç –ª–∏ –ø–∞—Ä–æ–ª–∏
  if (password !== confirmPassword) {
    return res.send("‚ùå –ü–∞—Ä–æ–ª–∏ –Ω–µ —Å–æ–≤–ø–∞–¥–∞—é—Ç");
  }

  try {
    // 2Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º email
    const checkUser = await db.query('SELECT * FROM "users" WHERE email = $1', [username]);
    if (checkUser.rows.length > 0) {
      return res.send("‚ùå –¢–∞–∫–æ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç");
    }

    // 3Ô∏è‚É£ –•–µ—à–∏—Ä—É–µ–º –ø–∞—Ä–æ–ª—å
    const hashed = await bcrypt.hash(password, saltRounds);

    // 4Ô∏è‚É£ –°–æ–∑–¥–∞—ë–º –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    const result = await db.query(
      'INSERT INTO "users"(email, password) VALUES ($1, $2) RETURNING *',
      [username, hashed]
    );
    const user = result.rows[0];

    // 5Ô∏è‚É£ –õ–æ–≥–∏–Ω–∏–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ —Ä–µ–¥–∏—Ä–µ–∫—Ç–∏–º
    req.login(user, (err) => {
      if (err) {
        console.log("–û—à–∏–±–∫–∞ –ª–æ–≥–∏–Ω–∞:", err);
        return res.redirect("/login");
      }
      res.redirect("/gamenotes");
    });
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏:", err);
    res.redirect("/register");
  }
});


